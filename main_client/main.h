/**
 * @brief  封装的汉字字符串渲染函数（专用于16x16规格汉字）
 */
static inline void Oled_DrawChineseString(uint8_t x, uint8_t y, const uint8_t *ptr_hz, uint32_t count)
{
    if (ptr_hz == NULL || count == 0) {
        return;
    }
    for (uint32_t i = 0; i < count; i++) {
        ssd1306_DrawRegion(x + (i * 16), y, 16, &ptr_hz[i * 32], 32);
    }
}

/* 控制 LED 状态 */
void SetLedState(pin_t led_pin, int turn_on)
{
    uapi_gpio_set_val(led_pin, turn_on ? GPIO_LEVEL_HIGH : GPIO_LEVEL_LOW);
}

/**
 * @brief I2C 主机引脚复用配置
 * @note  用于配置连接 AHT20 与 BH1750 的 I2C 总线引脚复用功能
 */
void app_i2c_init_pin(void)
{
    uapi_pin_set_mode(CONFIG_I2C_SCL_MASTER_PIN, CONFIG_I2C_MASTER_PIN_MODE);
    uapi_pin_set_mode(CONFIG_I2C_SDA_MASTER_PIN, CONFIG_I2C_MASTER_PIN_MODE);
}

/**
 * @brief LED 指示灯初始化
 * @note  配置红、绿、黄三色 LED 对应 GPIO 为输出模式，并默认熄灭
 */
void LedsInit(void)
{
    pin_t leds[] = {LED_RED_GPIO, LED_GREEN_GPIO, LED_YELLOW_GPIO};
    for (int i = 0; i < 3; i++) {
        uapi_pin_set_mode(leds[i], HAL_PIO_FUNC_GPIO);
        gpio_select_core(leds[i], CORES_APPS_CORE);
        uapi_gpio_set_dir(leds[i], GPIO_DIRECTION_OUTPUT);
        SetLedState(leds[i], 0);
    }
}

